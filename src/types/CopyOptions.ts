/**
 * Copy options.
 *
 * @export
 * @interface CopyOptions
 */
export default interface CopyOptions {
	/**
	 * Force to overwrite.
	 *
	 * @type {boolean}
	 * @memberof SafeCopyOptions
	 */
	overwrite: boolean;
	/**
	 * Display verbose information.
	 *
	 * @type {boolean}
	 * @memberof SafeCopyOptions
	 */
	verbose: boolean;
}
